# While loop 
# A while loop is used to execute a block of code repeatedly as long as a given condition is true.
# The syntax for a while loop is:
#
# while condition:
#     # code block to be executed

i = 1 # Initialization
while i <= 5: # Condition
    print("Mai hoon don ")
    i += 1 # Increment/Decrement
# The loop will continue to execute until the condition becomes false.
# In this case, the loop will print "Mai hoon don" five times.
#
