

# class in python 
# a class is a blueprint for creating objects.
# It defines a set of attributes and methods that the objects created from the class will have.

class Ritik:
    def config(self): # self is a reference to the current instance of the class
        print("ritik don hai ")
        
        
# creating an object of the class Ritik
ritik = Ritik()

# calling the config method of the Ritik class
ritik.config()  # Output: ritik don hai