# Return the absolute value of a number
def abs_value(number):
    """
    Returns the absolute value of a number.
    
    Parameters:
    number (int, float): The number to find the absolute value of.
    
    Returns:
    int, float: The absolute value of the input number.
    
    Example:
    >>> abs_value(-5)
    5
    >>> abs_value(3.14)
    3.14
    """
    return abs(number)